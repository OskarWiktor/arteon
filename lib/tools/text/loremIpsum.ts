export interface LoremStats {
  words: number;
  charsWithSpaces: number;
  charsWithoutSpaces: number;
  sentences: number;
  paragraphs: number;
  readingTime: string;
  bytes: number;
}

export function getLoremStats(text: string): LoremStats {
  const clean = text.replace(/<[^>]+>/g, '');
  const trimmed = clean.trim();
  if (!trimmed) {
    return {
      words: 0,
      charsWithSpaces: 0,
      charsWithoutSpaces: 0,
      sentences: 0,
      paragraphs: 0,
      readingTime: '0 s',
      bytes: 0,
    };
  }

  const words = trimmed.split(/\s+/).length;
  const charsWithSpaces = trimmed.length;
  const charsWithoutSpaces = trimmed.replace(/\s/g, '').length;
  const sentences = (trimmed.match(/[.!?]+/g) || []).length || 1;
  const paragraphs = trimmed.split(/\n\s*\n/).filter(Boolean).length || 1;
  const readingMinutes = words / 200;
  const bytes = new TextEncoder().encode(text).length;

  let readingTime: string;
  if (readingMinutes < 1) {
    readingTime = `${Math.max(1, Math.round(readingMinutes * 60))} s`;
  } else {
    readingTime = `${Math.round(readingMinutes)} min`;
  }

  return {
    words,
    charsWithSpaces,
    charsWithoutSpaces,
    sentences,
    paragraphs,
    readingTime,
    bytes,
  };
}

export function formatBytes(bytes: number): string {
  if (bytes < 1024) return `${bytes} B`;
  return `${(bytes / 1024).toFixed(1)} KB`;
}

const LOREM_OPENER = 'Lorem ipsum dolor sit amet, consectetur adipiscing elit.';

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function seededRandom(seed: number): () => number {
  let s = seed;
  return () => {
    s = (s * 16807) % 2147483647;
    return (s - 1) / 2147483646;
  };
}

/**
 * Selects a single element from `arr` using the provided random number generator.
 *
 * @param arr - The array to choose an element from
 * @param rng - A function that returns a pseudo-random number in the range [0, 1)
 * @returns A single element chosen from `arr`
 */
function pick<T>(arr: T[], rng: () => number): T {
  return arr[Math.floor(rng() * arr.length)];
}

/**
 * Capitalizes the first character of a string using locale-aware casing.
 *
 * @param s - The input string to capitalize
 * @returns The input string with its first character converted to uppercase according to locale; returns an empty string unchanged
 */
function capitalize(s: string): string {
  return s.charAt(0).toLocaleUpperCase() + s.slice(1);
}

/**
 * Selects a pseudorandom integer between min and max, inclusive.
 *
 * @param min - Lowest integer in the inclusive range.
 * @param max - Highest integer in the inclusive range.
 * @param rng - Random number generator that produces a float in [0, 1).
 * @returns An integer between `min` and `max`, inclusive.
 */
function randBetween(min: number, max: number, rng: () => number): number {
  return Math.floor(rng() * (max - min + 1)) + min;
}

// ---------------------------------------------------------------------------
// Sentence / paragraph builders
// ---------------------------------------------------------------------------

function buildSentence(
  bank: string[],
  rng: () => number,
  minWords: number,
  maxWords: number,
): string {
  const len = randBetween(minWords, maxWords, rng);
  const words: string[] = [];
  for (let i = 0; i < len; i++) {
    words.push(pick(bank, rng));
  }
  // Add a comma somewhere in the middle for natural flow
  if (len > 6) {
    const commaPos = randBetween(2, len - 3, rng);
    words[commaPos] = words[commaPos] + ',';
  }
  return capitalize(words.join(' ')) + '.';
}

function getLengthRange(length: LoremLength): {
  minSentences: number;
  maxSentences: number;
} {
  switch (length) {
    case 'short':
      return { minSentences: 2, maxSentences: 4 };
    case 'medium':
      return { minSentences: 4, maxSentences: 7 };
    case 'long':
      return { minSentences: 7, maxSentences: 12 };
  }
}

function buildParagraph(
  bank: string[],
  rng: () => number,
  length: LoremLength,
): string {
  const { minSentences, maxSentences } = getLengthRange(length);
  const sentenceCount = randBetween(minSentences, maxSentences, rng);
  const sentences: string[] = [];
  for (let i = 0; i < sentenceCount; i++) {
    sentences.push(buildSentence(bank, rng, 5, 15));
  }
  return sentences.join(' ');
}


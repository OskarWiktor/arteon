const POPULAR_CONVERTER_KEYS: ToolItemKey[] = ['jpgToWebpSimple', 'pngToWebpSimple', 'webpToJpg', 'webpToPng', 'pngToJpg', 'jpgToPng', 'svgToPng', 'bmpToJpg', 'jpgToAvif'];

/** Footer tools: all non-converter tools + 9 most popular converters */
export function getFooterTools(locale: Locale): ToolSectionItem[] {
  const nonConverters = TOOL_REGISTRY.filter((t) => t.section !== 'konwertery' && t.locales[locale]);
  const popularConverters = POPULAR_CONVERTER_KEYS.map((key) => TOOL_REGISTRY.find((t) => t.key === key)).filter((t): t is ToolDefinition => !!t && !!t.locales[locale]);

  return [...nonConverters, ...popularConverters].map((tool) => ({
    key: tool.key,
    href: getToolHref(tool.key, locale),
    title: tool.locales[locale]!.title,
    description: tool.locales[locale]!.description,
    image: tool.images?.[locale] ?? tool.image,
    icon: tool.icon,
    desktopOnly: tool.desktopOnly || undefined,
    carouselOrder: tool.carouselOrder,
  }));
}

/** Mobile nav tool sections: same as getToolsSections but limits konwertery to 9 popular */
export function getMobileToolsSections(locale: Locale): ToolsSection[] {
  return TOOL_SECTIONS.filter((section) => section.locales[locale]).map((section) => {
    const isConverters = section.key === 'konwertery';
    const tools = isConverters
      ? POPULAR_CONVERTER_KEYS.map((key) => TOOL_REGISTRY.find((t) => t.key === key)).filter((t): t is ToolDefinition => !!t && !!t.locales[locale])
      : TOOL_REGISTRY.filter((t) => t.section === section.key && t.locales[locale]);

    return {
      key: section.key,
      title: section.locales[locale]!.title,
      icon: section.icon,
      items: tools
        .filter((t) => !t.desktopOnly)
        .map((tool) => ({
          key: tool.key,
          href: getToolHref(tool.key, locale),
          title: tool.locales[locale]!.title,
          description: tool.locales[locale]!.description,
          image: tool.images?.[locale] ?? tool.image,
          icon: tool.icon,
          desktopOnly: tool.desktopOnly || undefined,
          carouselOrder: tool.carouselOrder,
        }))
        .sort((a, b) => (isConverters ? a.title.localeCompare(b.title, locale) : 0)),
    };
  });
}
